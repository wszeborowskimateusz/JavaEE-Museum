package pl.wszeborowski.mateusz.exhibit.model;

/**
 * A condition of a given exhibit
 *
 * @author wszeborowskimateusz
 */
public enum ExhibitCondition {
    /**
     * Exhibit in a terrible condition
     */
    TERRIBLE,

    /**
     * Exhibit in a bad condition
     */
    BAD,

    /**
     * Exhibit in a medium condition
     */
    MEDIUM,

    /**
     * Exhibit in a good condition
     */
    GOOD,

    /**
     * Exhibit in a very condition
     */
    VERY_GOOD,

    /**
     * Exhibit in a excellent condition
     */
    EXCELLENT
}
